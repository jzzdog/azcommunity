  # POST /messages
  # POST /messages.json
  def create
    @message = Message.new(message_params)
    @message.user_id = session[:user_id]
    @message.office_id = User.find_by(id: session[:user_id])
    @message.status = 1

    respond_to do |format|
      if @message.save
        
